import { _Object } from "../constants";
/**
 * Returns an array of the objects values.
 *
 * `Object.values` shorthand.
 *
 * @function objValues
 * @memberof Object
 * @since 1.0.0
 * @param {Object} obj
 * @returns {any[]}
 * @example
 * // returns [1, 2, 3]
 * objValues({a: 1, b: 2, c: 3})
 */
const objValues = _Object.values;

export default objValues;
